function Card(index, id, name, variant, e, x){
  this.index = index;
  this.id = id;
  this.name = name;
  this.variant = variant;
  this.rarity = x[0];
  this.maxLv = x[0] ? 45 + x[0] * 5 : 1;
  this.attribute = x[1];
  this.weapon = [x[2], x[3]];
  this.baseAtk = x[4] || 0;
  this.growth = new Fraction(x[5], 98);
  this.csBoost = x[6] || 0;
  this.tag = e.map(function(s, i){
    return splitTagNames(s, i);
  });
  this.effects = splitEffects(e[0], 0).concat(splitEffects(e[2], 1));
  this.limited = 1;
  if(!variant && LIMITED.indexOf(t(name, 0)) === -1) this.limited = 0;
  if(t(name, 0) === "トライヴ" && x[1] < 7) this.limited = 0;
}
Card.prototype = {
  toString: function(){
    var name = t(this.name) + t("/ ");
    if(!this.name) return LINE;
    if(!this.baseAtk) name = "× " + name;
    if(this.rarity < 3) return name + "(" + ATTRIBUTE[this.attribute] + ")";
    if(this.variant) return name + "(" + t(this.variant).slice(0, -2) + ")";
    return name + "☆" + this.rarity;
  },
  getValue: function(lv){
    return this.growth.mul((lv || 1) - 1, 1).add(this.baseAtk, 1).round();
  },
  canEquip: function(x){
    if(!this.index || !x.index) return true;
    if(x.chara.length && x.chara.indexOf(this.index) !== -1) return true;
    if(x.rarity & (1 << this.rarity)) return true;
    if(x.weapon & (1 << this.weapon[0])) return true;
    if(x.attribute & (1 << this.attribute)) return true;
    return false;
  }
};
Card.createList = function(a){
  var name = "";
  var e = ["", "", "", "", "", ""];
  var idH = 0;
  var idL = 0;
  return a.map(function(v, i){
    var x = v.shift();
    var variant = "";
    if(x && x[0] !== "@"){
      name = x;
      idL = 0;
      idH++;
    }else{
      variant = x.slice(1);
      e.forEach(function(s, j){
        if(v[j] === "*") v[j] = s;
      });
    }
    e = v.slice(0, 6);
    if(v[1] < 3){
      idL = v[2];
    }else{
      idL++;
    }
    return new Card(i, idH * 10 + idL, name, variant, e, v.slice(6));
  });
};
