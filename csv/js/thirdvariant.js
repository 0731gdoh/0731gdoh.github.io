const countDays = (list) => {
  let i = 0;
  for(const row of list){
    const since = toDate(row[1]);
    const until = toDate(row[2]);
    const days = (until.getTime() - since.getTime()) / (1000 * 60 * 60 * 24);
    row.push(row[2] ? "○" : "");
    row.push((days | 0) + "");
    row.unshift(++i + "");
  }
  list.unshift(["#", "名前", "実装", "三枠目", "済", "日数"]);
  return list;
};

